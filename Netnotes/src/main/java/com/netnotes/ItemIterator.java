package com.netnotes;

public class ItemIterator {

    private int m_i;
    private int m_j;

    public ItemIterator() {
        m_i = 0;
        m_j = 0;
    }

    public void setI(int i) {
        m_i = i;
    }

    public int getI() {
        return m_i;
    }

    public int getJ() {
        return m_j;
    }

    public void setJ(int j) {
        m_j = j;
    }

}
